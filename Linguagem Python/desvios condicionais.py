num = int(input())

if num < 0: # expressão
	print('número negativo')
elif num < 10: # else if
	print('número positivo menor que 10')
else: # python não toma ; () ou {} como obrigatório
	print('número positivo maior que 10')

if num % 2 == 0:
	print("número é par")


